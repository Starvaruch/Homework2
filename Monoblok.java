import java.util.*;

public class Monoblok {
    private Computer comp;
    
    public Monoblok(){
        this.comp = new comp();
    }
    public String getprocessor() {
        return comp.getprocessor();
    }
    public String getOZY() {
        return comp.getOZY();
    }
    public String getdisk() {
        return comp.getdisk();
    }
    public String getmonitor() {
        return comp.getmonitor();
    }
}
